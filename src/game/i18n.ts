export function resolveLocale(languages: readonly string[]): GameLocale {
  for (const language of languages) {
    const normalized = language.trim().replaceAll('_', '-').toLowerCase()
    const base = normalized.split('-')[0]
    if (base === 'zh' && !normalized.startsWith('zh-tw') && !normalized.startsWith('zh-hk')) {
      return 'zh-CN'
    }
    if (normalized === 'pt' || normalized === 'pt-br') return 'pt-BR'
    if (base === 'en' || base === 'fr' || base === 'it' || base === 'de' || base === 'es') {
      return base
    }
    if (base === 'ja' || base === 'ko' || base === 'ru' || base === 'tr') return base
  }
  return 'en'
}

export function detectLocale(): GameLocale {
  const queryLanguage =
    typeof location === 'undefined' ? null : new URLSearchParams(location.search).get('lang')
  if (queryLanguage) return resolveLocale([queryLanguage])
  if (typeof navigator === 'undefined') return 'en'
  const languages = navigator.languages.length > 0 ? navigator.languages : [navigator.language]
  return resolveLocale(languages)
}
